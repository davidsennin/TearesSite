				<b>Tear Vertical:</b> tapeçarias de parede e gobelin.<br>
				<b>Tear de Pregos:</b> para trabalhos simples com crianças em Oficinas de Arte ou na Educação Especial.<br>
				<b>Tear de Faixas:</b> para confecção de faixas e cintos.<br>
				<b>Tear de Cartão:</b> mesmo emprego que o anterior.<br>
				<b>Tear de alto-liço:</b> para tapeçarias em geral.<br>
				<b>Tear de Padronagem:</b> para o estudo de padronagem de tecidos. Funciona como o tear de pedal, sòmente é menor e manual.<br>
				<b>Tear de papelão:</b> para brincar com as crianças.<br>
				<b>Tear Pente Liço:</b> também denominado tear de mesa, usado para a confecção de tapetes, faixas, e tecidos para vestuario, bolsas e utilitários.<br>
